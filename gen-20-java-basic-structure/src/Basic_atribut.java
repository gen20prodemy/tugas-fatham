public class Basic_atribut {//nama kelas
    public static void main (String[] args){//method main
        String nama = "Fatham"; //String
        int gen = 20; //Integer
        float tahun = 2.24f; //Floating number, precision up to 6 or 7 decimal digits
        double koma = 19.21d; //Double variables, precision up to 15 digits
        char depan = 'M'; //Character

        int x = 35;// inisialisasi variabel int x
        int y = 15;// inisilisasi variabel int y
        int total = x+y;// inisialisasi int total : x+y
        System.out.println(depan+" "+nama+" "+gen+","+tahun+" dan "+koma);//print out variabel to console
        System.out.println("Total x+y : " + total);//print out variabel total

    }
}
